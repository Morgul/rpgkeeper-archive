var path = require('path');
var app = require('omega-wf').app;
var views = require('./lib/views');

//----------------------------------------------------------------------------------------------------------------------

app.static.add(
    {
        url: '/client',
        path: path.join(__dirname, 'client')
    }
);

//----------------------------------------------------------------------------------------------------------------------

app.router.add(
    {
        url: '/',
        all: views.index
    }
);

